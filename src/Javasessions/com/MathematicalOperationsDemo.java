package Javasessions.com;

public class MathematicalOperationsDemo {

	public static void main(String[] args) {
		        
		        
		        System.out.println(2+2);//4
		        System.out.println(2-2);//0
		        System.out.println(2*2);//4
		        System.out.println(2/2);//1
		        System.out.println(2%2);//0
		        
		        System.out.println(6/2);//3
		        System.out.println(9/2);//4.5
		        System.out.println(6.0/2);//3.0
		        System.out.println(9/2.0);//4.5
		        System.out.println(9.0/2.0);//4.5
		        System.out.println(9.0/2.0);//4.5
		        
		        
		        
		        System.out.println(0/5);//0
		        //System.out.println(5/0);//ArithmeticException: / by zero
		        
		        
		        System.out.println(6.0/0);//Infinity
		        
		        System.out.println(6/0.0);//Infinity
		        
		        System.out.println(0.0/0.0);//NaN
		        System.out.println(0.0/3.0);//0.0
		        
		        System.out.println(0.0/0);//NaN
		        
		        System.out.println('b'/2);//49
		        System.out.println(6%2);//0
		        System.out.println("Thanks");
		        

	}

}
